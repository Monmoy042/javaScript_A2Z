// Logical Operator in JavaScript
/*
    Operator    Description
       ||       Logical OR
       &&       Logical AND
       !        Logical NOT
*/

// Logical OR
console.log(false||true); // Output will be true.
let x = 10;
let y = 20;
let z = x || y;
console.log(z)

// Logical AND
console.log(false && true);
let a = -2;
let b = 2;
console.log(a>0 && a<2); // Output will be false

// Logical NOT
let n1 = 10;
console.log(n1!=20);
console.log(n1 !=10);